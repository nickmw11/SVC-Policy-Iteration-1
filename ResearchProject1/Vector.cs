namespace ResearchProject1
{
    public class Vector
    {
        public int x { get; set; }
        public int y { get; set; }

        public Vector()
        {
            x = 0;
            y = 0;
        }

        public Vector(int _x, int _y)
        {
            x = _x;
            y = _y;
        }
    }
}